/*! \note initialize platform
 *  \param none
 *  \retval true platform initialization succeeded.
 *  \retval false platform initialization failed
 */
bool platform_init( void )
{
    bool bResult = false;

    do {
        /*! initialize hardware abstract layer */
        if ( !hal_init() ) {
            break;
        }

        /*! initialize system services */
        if ( !service_init() )  {
            break;
        }

        bResult = true;
    } while(false);

    return bResult;
}

